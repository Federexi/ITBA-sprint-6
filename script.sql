-- Agrego tablas tipo de cliente, tipo de cuenta y marca de tarjeta
CREATE TABLE tipo_cliente (
	customer_id INTEGER NOT NULL,
	cu_type TEXT NOT NULL DEFAULT CLASSIC,
	FOREIGN KEY (customer_id) REFERENCES cliente(customer_id)
);

CREATE TABLE tipo_cuenta (
	account_id INTEGER NOT NULL,
	ac_type TEXT NOT NULL DEFAULT 'Caja de ahorro en pesos',
	FOREIGN KEY (account_id) REFERENCES cuenta(account_id)
);

CREATE TABLE marca_tarjeta (
	card_id INTEGER NOT NULL,
	card_brand TEXT NOT NULL DEFAULT 'VISA',
	FOREIGN KEY (card_id) REFERENCES tarjeta(card_id)
);

-- Agrego tabla tarjeta 
CREATE TABLE tarjeta ( 
	card_id INTEGER PRIMARY KEY AUTOINCREMENT,
	account_id INTEGER NOT NULL,
	customer_id INTEGER NOT NULL DEFAULT 1,
	card_number TEXT UNIQUE CHECK(length(card_number)<=20),
	CVV TEXT NOT NULL,
	valid_from DATE NOT NULL,
	expiration_date DATE NOT NULL,
	card_type TEXT NOT NULL,
	FOREIGN KEY (account_id) REFERENCES cuenta (account_id) ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY (customer_id) REFERENCES cliente (customer_id) ON UPDATE CASCADE ON DELETE CASCADE
);

